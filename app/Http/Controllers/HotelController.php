     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        // equals: Select * from store where id = ?
        $hotel = Hotel::with('law')->findOrFail($id);

        // chatgpt
        $gptResponse = Chatgpt::where('keyword', $hotel->ch_name)->get()->first();

        $dictData = [
            $hotel->ch_name => !is_null($gptResponse) ? $gptResponse->respond : 'What are u talking about'
        ];


        return view('hotel/show', compact("hotel", "dictData"));
    }

